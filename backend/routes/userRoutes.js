import express from "express";
import asyncHandler from "express-async-handler";
const router = express.Router();
import generateToken from "../utils/generateToken.js";
import User from "../models/userModel.js";
import { protect } from "../middleware/authMiddleware.js";

// @desc    Auth user & get token
// @route   POST /api/users/login
// @access  Public
const authUser = asyncHandler(async (req, res) => {
  const { email, password } = req.body; //destruct password and email from the POST request body

  const user = await User.findOne({ email }); //find te user by "email" id
  //matchPassword method coming from UserModel.js file
  if (user && (await user.matchPassword(password))) {
    //if the user exist and password matched
    res.json({
      _id: user._id,
      name: user.name,
      email: user.email,
      isAdmin: user.isAdmin,
      token: generateToken(user._id),
    });
  } else {
    res.status(401);
    throw new Error("Invalid email or password");
  }
});

// @desc    Get user profile
// @route   GET /api/users/profile
// @access  Private
const getUserProfile = asyncHandler(async (req, res) => {
  const user = await User.findById(req.user._id); //req.user._id = current logged in user

  if (user) {
    res.json({
      _id: user._id,
      name: user.name,
      email: user.email,
      isAdmin: user.isAdmin,
    });
  } else {
    res.status(404);
    throw new Error("User not found");
  }
});

// @desc    Register a new user
// @route   POST /api/users
// @access  Public
const registerUser = asyncHandler(async (req, res) => {
  const { name, email, password } = req.body;

  const userExists = await User.findOne({ email });

  if (userExists) {
    res.status(400);
    throw new Error("User already exists");
  }

  const user = await User.create({
    name,
    email,
    password,
  });
  //if the user created is done then....
  if (user) {
    res.status(201).json({
      _id: user._id,
      name: user.name,
      email: user.email,
      isAdmin: user.isAdmin,
      token: generateToken(user._id),
    });
  } else {
    res.status(400);
    throw new Error("Invalid user data");
  }
});

router.route("/").post(registerUser);
router.post("/login", authUser);
//without passing "protect" middleware getUserProfile won't executed
router.route("/profile").get(protect, getUserProfile);

export default router;
